"""Provide basic arithmetic utilities."""
def square(x):
    """Return x squared."""
    return x * x
def cube(x):
    """Return x cubed."""
    return x * x * x
class MathUtils:
    """Provide arithmetic helper methods."""
    def add(self, a, b):
        """Return the sum of a and b."""
        return a + b
    def subtract(self, a, b):
        """Return the difference of a and b."""
        return a - b