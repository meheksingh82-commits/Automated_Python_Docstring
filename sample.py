def square(number):
    """Return the square of a number."""
    return number * number


def cube(number):
    """Function cube."""
    return number ** 3


class MathUtils:
    """Class MathUtils."""
    def add(self, a, b):
        """Add two numbers."""
        return a + b

    def subtract(self, a, b):
        """Function subtract."""
        return a - b